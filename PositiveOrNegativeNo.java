package controlFlowStatemente;

public class PositiveOrNegativeNo {

	public static void main(String[] args) {

		int num = 10;

		System.out.println("Check the number is positive or negative");

		if (num >= 0) {
			System.out.println(num + " Number is a Positive Number...");
		} else {
			System.out.println(num + " Number is a Negative number...");
		}
	}

}
