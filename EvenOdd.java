package controlFlowStatemente;

public class EvenOdd {

	public static void main(String[] args) {

		int num = 10;

		System.out.println("Ckeck the Number is Even or Odd");

		if (num % 2 == 0) {
			System.out.println(num + " Number is a Even Number");
		} else {
			System.out.println(num + " Number is a Odd Number");
		}
	}

}
